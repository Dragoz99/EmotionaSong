package ClientES;

public class Emozioni {
    private String note;
    private int score;
    private String tag;
    private String emozioni;

    public Emozioni(){

    }


    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getEmozioni() {
        return emozioni;
    }

    public void setEmozioni(String emozioni) {
        this.emozioni = emozioni;
    }
}
